namespace dotNetTips.Utility.Standard.Common
{
	/// <summary>
	/// Class ControlChars.
	/// </summary>
	public static class ControlChars
	{
		/// <summary>
		/// @ Symbol.
		/// </summary>
		public const char At = '@';

		/// <summary>
		/// Back.
		/// </summary>
		public const char Back = '\b';

		/// <summary>
		/// Back Slash.
		/// </summary>
		public const char Backslash = '\\';

		/// <summary>
		/// The colon.
		/// </summary>
		public const char Colon = ':';

		/// <summary>
		/// Comma.
		/// </summary>
		public const char Comma = ',';

		/// <summary>
		/// CR.
		/// </summary>
		public const char Cr = '\r';

		/// <summary>
		/// The cr.
		/// </summary>
		public const char CR = '\r';

		/// <summary>
		/// CRLF.
		/// </summary>
		public const string CRLF = "\r\n";

		/// <summary>
		/// The default separator.
		/// </summary>
		public const string DefaultSeparator = ", ";

		/// <summary>
		/// The dot (period).
		/// </summary>
		public const char Dot = '.';

		/// <summary>
		/// The double quote.
		/// </summary>
		public const string DoubleQuote = "''";

		/// <summary>
		/// Empty String.
		/// </summary>
		public const string EmptyString = "";

		/// <summary>
		/// The end angle bracket.
		/// </summary>
		public const char EndAngleBracket = '>';

		/// <summary>
		/// The end comment.
		/// </summary>
		public const char EndComment = ')';

		/// <summary>
		/// The start square bracket.
		/// </summary>
		public const char EndSquareBracket = ']';

		/// <summary>
		/// Form feed.
		/// </summary>
		public const char FormFeed = '\f';

		/// <summary>
		/// Forward slash.
		/// </summary>
		public const char ForwardSlash = '/';

		/// <summary>
		/// The Line Feed.
		/// </summary>
		public const char LF = '\n';

		/// <summary>
		/// New line.
		/// </summary>
		public const char NewLine = '\n';

		/// <summary>
		/// Null character.
		/// </summary>
		public const char NullChar = '\0';

		/// <summary>
		/// Quote.
		/// </summary>
		public const char Quote = '\"';

		/// <summary>
		/// The single quote.
		/// </summary>
		public const char SingleQuote = '\'';

		/// <summary>
		/// Space.
		/// </summary>
		public const char Space = ' ';

		/// <summary>
		/// The start angle bracket.
		/// </summary>
		public const char StartAngleBracket = '<';

		/// <summary>
		/// The start comment.
		/// </summary>
		public const char StartComment = '(';

		/// <summary>
		/// The start square bracket.
		/// </summary>
		public const char StartSquareBracket = '[';

		/// <summary>
		/// Tab.
		/// </summary>
		public const char Tab = '\t';

		/// <summary>
		/// Underscore.
		/// </summary>
		public const char Underscore = '_';

		/// <summary>
		/// Vertical tab.
		/// </summary>
		public const char VerticalTab = '\v';
	}
}
